<h3>B.シリカ系特殊バインダー　事業内容</h3>
<p>弊社は、(株)ジャパンナノコートの開発した特殊シリカバインダーの特許を共同で出願、販売もさせて頂いております。</p>
<p>特殊シリカバインダーは、基材表面の微細な凹凸を利用する密着メカニズムであるため、ガラスはもちろんポリカ・PET・PMMA・ABS・FRPなどの樹脂や金属といった様々な基材に密着する特殊なバインダーです。<br />
<a href="../../pdf/binder.pdf" target="_blank">無機バインダー＆無機プライマー(PDF:315KB)</a></p>
