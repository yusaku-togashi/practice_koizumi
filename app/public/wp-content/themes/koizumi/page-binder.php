<h3>２)添加物の例</h3>
<ol>
<li>&#9312;　プラチナ　：当無機バインダーのシリカ微粒子はもともと親水性を持っていますが、プラチナの微粒子を添加する事で親水性を強化できます。また抗菌性付与のために使用する場合もあります。</li>
<li>&#9313;　酸化スズ　：帯電防止性の付与。建材の防汚塗料や火山灰による汚れ防止塗料用バインダーに添加し、高い効果を発揮しております。バインダー中のシリカだけで、10^12Ω程度の導電性を持ちますが、酸化スズを加えることで10^9Ω～10^8Ω程度（最大10^7Ω）まで導電性を上げ、帯電防止性を強化することが可能です。</li>
<li>&#9314;　ナノダイヤ　　：低摩擦化による耐摩耗性の向上。過酷な環境を想定する場合に使用します。</li>
<li>&#9315;　界面活性剤　：無機100%という特徴は損なわれますが、水系バインダーを、水をはじく疎水性の基材に適応させるために使用します。</li>
</ol>
<p>(*)屈折率の調節は、上記のような添加物の比率調節と膜厚によって行います。</p>
<h4>■添加物の応用例</h4>
<li>CNT（カーボンナノチューブ）　：
CNTを基材に密着させるためのバインダーとしてご使用いただけます。CNTの性能や分散液の特性によって左右されますが、10^3Ω以上の高い導電性を基材表面に付与する事が可能となります。</li>

