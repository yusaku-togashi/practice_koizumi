<li>&#9314; このバインダーに使用されているシングルナノサイズ（1~10nm）の微粒シリカには親水性があるため、基材表面の親水性付与の改質や、帯電防止効果による防汚性能の付与が可能です。
</li>
<li>&#9315; 酸化スズ・プラチナ・CNT等様々なナノサイズの素材を添加する事で、基材表面に添加した素材の特殊機能を付与をしたり、親水性や帯電防止効果を強化する事が出来ます。</li>
<li>&#9316; シリカ粒子が主成分のため、塗膜は無色透明です（Haze値 0.5以下）。添加物を導入する際も、添加物が光を散乱しない程度に十分小さければ透明性は保たれます。</li>
<li>&#9317; 屈折率の調整が可能です。低屈・高屈双方でのカスタマイズをする事が出来ます。</li>
</ol>


