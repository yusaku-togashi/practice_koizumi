<a name="lub03" id="lub03"></a>
<h3>３)製品の販売形態</h3>
<p>製品の販売形態は、メタノール系及び水系のバインダーとしてのご提供が主となります。<br />
添加物の配合をあらかじめ調製してある、コート剤という形でも供給させて頂いております。<br />
お客様のご要望に応じて、下記のカスタマイズの例にて紹介するようなコート剤に、さらにカスタマイズを行う事も可能です。<br />
<a href="../../pdf/japan_nano.pdf" target="_blank">ジャパンナノコート常温コーティング(PDF:990KB)</a></p>
<h4>■カスタマイズの例</h4>
<h5>&#9312;AS-LR（Anti-static Low Refraction）コート剤</h5>
<p>AS-LRコート剤は、帯電防止、低屈折の特徴を持ったメタノール系コーティング剤です。可視光透過率の向上と防汚性能が必要な用途に適しています。AS（帯電防止）効果・LR（低反射）効果を、1種類の常温・速乾性のあるコーティング液で実現できます。</p>
<h5>ご使用例</h5>
<ul>
<li>太陽光発電パネルに塗布する事で、帯電防止効果によって汚れの付着を防止します。また、屈折率を調節して太陽光の透過率を上げることができます。これらの効果によって、太陽光発電パネルの発電効率を向上させることができます。</li>
<li>透明樹脂の監視カメラカバーや屋外センサーカバーに使用する事で、防汚効果や可視光透過率の向上によって視野の向上・センサー感度の維持が期待できます。また、親水性の塗膜を形成するので水の接触角が下がるため、結露や雨滴によって付着した水滴で視野が悪化する事を防止できます。
<br /><a href="../../pdf/aslr.pdf" target="blank">AS－LRコート　プレゼン用タイプ（PDF:1.05MB）</a></li>
</ul>
<h5>&#9313;水系防汚コート剤</h5>
<p>水系バインダーをもとに、添加物を調製して、基材への密着性、親水性、帯電防止性を最適化したコーティング剤です。メタノール系と同じく、常温で塗布が可能で速乾性があるため、現場での施工も可能です。食器に使用できるほど人と環境に優しい製品です。</p>
<h5>ご使用例</h5>
<ul>
<li>親水性表面改質用のプライマー　：樹脂表面に塗布する事で、親水性の塗料やコーティング剤用のプライマーとして使用可能です。</li>
<li>太陽光発電パネルの防汚コート　：取り扱いが簡便で、現場施工も可能です。LR加工がすでになされた太陽光発電パネルに、光の透過率を損なう事なく容易に防汚性能を付与できます。</li>
<li>窓ガラスの防汚コート　：透明性を損なう事なく、簡便な方法で防汚性能を付与できます。親水性の塗膜なので、防曇効果も発揮します。</li>
<li>各種建材・タイルの防汚コート　：排気ガスや雨滴等による建材の汚れを防止できます。桜島の火山灰による建材の黒変を防止した実績もあります。</li>
<li>金属への親水性防汚コート　：刷り込むように塗布する事で、金属表面にも親水性の塗膜を形成する事ができます。油汚れを防止する効果があります。</li>
</ul>
