<a name="lub01" id="lub01"></a>
<h3>１)ルブリコンＡ-112Ｍとは</h3>
<ol>
<li>&#9312; 国土交通省NETIS登録製品。国が認めた唯一の全石油系燃料に対応した燃料添加剤です。
</li>
<li>&#9313; 植物性酵素を主原料とする新しい発想の燃料添加剤です。<br />
<span>環境に優しい葉緑素の一種であるクロロフィルa/bに含まれる酵素により、</span><br />
<ul>
<li>1、燃料油中のスラッジを分解溶解</li>
<li>2、酸素を取り込む性質が有り、燃焼性を向上させる</li>
</ul>
</li>
<li>&#9314; 内燃機関各部、*1外燃機関各部および補機類に対し安全であることはもちろん、機関各部に対して洗浄効果があります。
<p>石油系（軽油、重油、原油など）を燃料とする船舶、重機/建機、製紙工場などのボイラー、火力発電所に使用。
</p>
</li>
<li>&#9315; 注入により低温での完全燃焼を実現できるようになるため、*2PMや煤煙の発生を７０％低減した実績があります。<br />
 加えて排ガスのクリーン化、煤煙の低減（透明化）を実現します。</li>
<li>&#9316; スラッジを除去。酵素成分が触媒となり、添加後スラッジを燃焼可能なまで溶解させます。</li>
<li>&#9317; 燃料油の分子を細分子化、分子が酸素を取り込んで、質の良い燃えやすい燃料に改質します。<br />
 燃焼効率の良さは燃費の改善に貢献します。
</li>
<li>&#9318; 一度だけではなく、継続して添加することで、機関内部、燃料系統の各機器がクリーン化されていくことで、機関全体の性能アップが実現します。
</li>
<li>&#9319; 粗悪燃料により航海中の停船を余儀なくされるような大型船舶における燃料油に関する非常事態にも対処することが出来、さらに未然に防ぐ予防保全用にも効果があります。
</li>
</ol>
<ol>
<li>*1 外燃機関のうち石油系燃料の燃焼によるエネルギーを動力源とする機関に注入します。</li>
<li>*2 PM＝Particulate Matter：粒子状物質</li>
</ol>


