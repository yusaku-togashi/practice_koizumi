<h3>A. 省エネ事業　事業内容</h3>
<p>ルブリコンＡ-112Ｍの販売</p>
<p>弊社は、環境保全・クリーンエネルギー対策の一環として数年前より太陽光発電・土壌改良等に取り組んでまいりましたが「エネルギーコストの削減」という業界最大の課題の解決に向けて
エネルギーの効率化・促進の為の事業にも参画，アジアネットワーク株式会社で開発した全石油に対応した燃料添加剤ルブリコンＡ-112Ｍを販売しております。
</p>
